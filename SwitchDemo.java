
public class SwitchDemo {
    public static void main(String [] args){
        int i;
        for(i=0;i<3;++i)
            switch(i){
                case 0:
                    System.out.println("i is 0");
                    break;
                case 1:
                    System.out.println("i is 1");
                    break;
                default:
                    System.out.println("i is 2");
                    
            }



    }

}